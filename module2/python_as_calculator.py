#  we can do any mathematical operation in python 
print(2+3)
print(2-3)
print(2*3)
print(10%2)     #modulo % = it will gives us the remainder after dividing 
print(10/2)     #float division , it will gives us the result in float(floating point)

print(10//2)   #inter division , it will gives us the result in integer
print(2**2)   #2**2 means 2 to the power 2 , means square of 2 
print(2**3)    # 2**3 means 2 to the power 3 , means cube of 2
print(2**0.5)  #square root of 2

print(round(2**0.5,3))
# round function will round off the value of 2**0.5 upto 3 decimal place 